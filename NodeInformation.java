
/**
 * Classe onde se guarda as informacoes dos nos da arvore
 * @author omar
 *
 */
public class NodeInformation {
	
	public String image;
	public int linePosition;
	public int columnPosition;
	
	/**
	 * Constroi a informacao baseada num token 
	 * @param t - Token a ser referido
	 */
	public NodeInformation(Token t){
		image=t.image;
		linePosition= t.beginLine;
		columnPosition = t.beginColumn;
	}
}
